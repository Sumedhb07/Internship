# math Module
# Task: Create a program that:
# Asks the user for a number.
# Prints:
# Square root of the number
# Ceiling and floor values
# Sine of the number (in radians)
# Functions to use: sqrt, ceil, floor, sin
import math
num = int(input("Enter a number for square root "))
print(f"quare root of the number:-{math.sqrt(num)}")
print(f"Ceiling and floor values:- {math.ceil(num)}")
print(f"Sine of the number (in radians):- {math.sin(num)}")
