#define Arithmetic operation using Function
def arithmetic_operation(a , b):
  print(f"sum is : {a + b}")
  print(f"sub is : {a - b}")
  print(f"mul is : {a * b}")
  print(f"div is : {a / b}")
  print(f"mod is : {a % b}")
  print(f"flow div is : {a // b}")
  print(f"Exponential is : {a ** b}")

arithmetic_operation(50 , 27)

