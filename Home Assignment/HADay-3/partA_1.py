#day3 PartA:Arithmetic practice
#1.Take two number from the user and parform
A=int(input("Enter the Value for A : "))
B=int(input("Enter the value for B : "))
#Addition 
sum=A+B
print(f"The Addition of {A} & {B} is {sum}")

#Subtraction 
sub=A-B
print(f"The Subtraction of {A} & {B} is {sub}")

#Multiplication 
mul=A*B
print(f"The Multiplication of {A} & {B} is {mul}")

#Division
div=A/B
print(f"The Division of {A} & {B} is {div}")

#Modulus
mod=A%B
print(f"The Modulus of {A} & {B} is {mod}")

#flow Division 
flowd=A//B
print(f"The flow division of {A} & {B} is {flowd}")

#Exponential
expon = A ** B
print(f"The Exponential of {A} & {B} is {expon}")

